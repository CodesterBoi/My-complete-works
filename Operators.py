#Comparison Operators:
g = 32
h = 96

print(g==h)
print(g>h)
print(g<h)
print(g>=h)
print(g<=h)
print(g!=h)

#Logical Operators:
i = (15 > 12 and 47 < 82)
j = (15 < 12 or 47 > 82)
k = not(47 < 15)

print(i)
print(j)
print(k)

